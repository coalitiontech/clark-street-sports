function codeAddress(address) {
  if (typeof google == "undefined") {
    return false;
  }
  var store_geocoder = new google.maps.Geocoder();
  store_geocoder.geocode({ 'address': address }, function (results, status) {
    if (status == 'OK') {
      store_map.setCenter(results[0].geometry.location);
    } else {
      return false;
    }

    var near_markers = [];
    store_markers.forEach(function (marker) {
      var marker_position = new google.maps.LatLng(marker.lat, marker.lng);
      var distance = google.maps.geometry.spherical.computeDistanceBetween(results[0].geometry.location, marker_position);
      if (store_radius_meters > distance) {
        marker.distance = parseFloat(distance / 1000).toFixed(2) + 'km';
        near_markers.push(marker);
      }
    });
    generateResultHTML(near_markers);
  });
}

function generateResultHTML(results) {
  jQuery('.stores__results').empty();
  results = results.sort(function (a, b) {
    return parseFloat(a.distance) - parseFloat(b.distance);
  });
  results.forEach(function (marker) {
    var $template = jQuery(jQuery('#result-template').html()).clone();
    var url = "https://www.google.com/maps/dir/?api=1";
    var destination = "&destination=" + marker.lat + "," + marker.lng;
    var newUrl = url + destination;
    $template.attr({
      'data-lat': marker.lat,
      'data-lng': marker.lng
    });
    $template.find('.title').text(marker.name);
    $template.find('.address').html(marker.address);
    $template.find('.distance').html(marker.distance);
    $template.find('.phone a').attr('href', 'tel:' + marker.phone).text(marker.phone);
    $template.find('.hours .inner').html(marker.hours);
    $template.find('.view-map .directions').attr('href', newUrl);
    jQuery('.stores__results').append($template);

    $('.stores__results').niceScroll({
      cursorcolor: '#d8d8d8',
      autohidemode: false
    });
  });
  if (results.length == 0) {
    jQuery('.stores__results').append('<p class="error">No locations found, please try another search.</p>');
  }
}

